Avant toute chose, revenons sur la notion de « service ». Un service consiste à mettre à disposition une capacité technique, intellectuelle ou un travail, directement utile pour l’usager, sans transformation de matière.

Une prestation de service peut aussi se définir par le fait que le prestataire ne fournit pas de bien tangible à son client.

La séparation est parfois difficile à faire. C'est le cas par exemple dans le secteur du bâtiment où l'on sépare l'achat/revente des matériaux et le travail de la main d'oeuvre sur ceux-ci (pour des raisons de TVA la plupart du temps).

A l’inverse, on considère qu’un restaurant fournit un service, alors qu’on pourrait considérer qu’une partie de ce qu’il vend n’est pas du service…

Qu'est-ce qu'un prestataire de services ?
Il s’ensuit qu’un prestataire de services est toute personne physique ou morale qui propose des services.

Par exemple en agriculture, une entreprise pourra avoir recours à un contrat de prestation de services agricoles pour déléguer les travaux d'une exploitation. Le prestataire sera alors une entreprise spécialisée, un professionnel indépendant, qui garantira une prestation.

Nous pouvons également citer les prestataires de services informatiques, de conseil, d’aide à domicile, de santé, de logistique, de location d’outil ou de véhicule, de services bancaires ou marketing, etc…

Contrat d'entreprise ou contrat de prestation de services ?
Le contrat de prestation de services est un contrat d'entreprise. Le contrat d'entreprise est une convention par laquelle un maître d'ouvrage (le client) charge un maître d'oeuvre (le prestataire) d’exécuter un travail moyennant une rémunération. Le prestataire fournit son travail en toute indépendance et sans représenter le client. Il n'y a aucun lien de subordination entre le client et le prestataire (comme c'est le cas pour un contrat de travail entre l'employeur et son salarié). 

Contrat de prestation de services et contrat de travail
Comme nous venons de le dire plus ci-dessus, le contrat de prestation de services n’est pas un contrat de travail. La principale différence tient en l'absence de lien de subordination. Lors de l’exécution d'un contrat de prestation de services, le prestataire est indépendant.

Si toutefois un lien de subordination est constaté, il existe alors un risque de requalification du contrat de prestation de services en contrat de travail. Ce risque de requalification dépendra des circonstances dans lesquelles la prestation est réalisée. 

Contrat de prestation de services et la mise à disposition de personnel
Une entreprise peut mettre à la disposition d'une autre société un ou plusieurs de ses salariés, on parle alors de mise à disposition de personnel.